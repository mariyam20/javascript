var a = 10;
console.log(a); //Output: 10
a = 100;
console.log(a); //Output: 100
a += 10; //a=a+10
console.log(a); //Output: 110
a -= 10; //a=a-10
console.log(a); //Output: 100
a *= 10; //a=a*10
console.log(a); //Output: 1000
a /= 10; //a=a/10
console.log(a); //Output: 100
a %= 10; //a=a %10
console.log(a); //Output: 0